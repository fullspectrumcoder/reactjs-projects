import React from "react";

const Listing = (props) => {
  const { data, yearly } = props;

  return (
    <div className="flex-listing">
      {data.map((item, index) => {
        return (
          <div className="list-box" key={index}>
            <div className="img">
              <img src={item.img} alt="" />
            </div>
            <h2 className="price">${yearly ? `${item.price*12*70/100}` : `${item.price}`}</h2>
            <h4 className="name">{item.title}</h4>
            <ul>
              {item.list.map((item, index) => {
                return <li key={index}>{item}</li>;
              })}
            </ul>
            <button className="btn-purchase">{item.button}</button>
          </div>
        );
      })}
    </div>
  );
};

export default Listing;
